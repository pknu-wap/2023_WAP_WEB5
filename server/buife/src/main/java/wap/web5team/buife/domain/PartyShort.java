package wap.web5team.buife.domain;

public class PartyShort {
    int partyPk;
    String partyName;
    String festName;
    String festAddress;
    String partyDetail;
    String festEnd;

    public int getPartyPk() {
        return partyPk;
    }

    public void setPartyPk(int partyPk) {
        this.partyPk = partyPk;
    }

    public String getPartyName() {
        return partyName;
    }

    public void setPartyName(String partyName) {
        this.partyName = partyName;
    }

    public String getFestName() {
        return festName;
    }

    public void setFestName(String festName) {
        this.festName = festName;
    }

    public String getFestAddress() {
        return festAddress;
    }

    public void setFestAddress(String festAddress) {
        this.festAddress = festAddress;
    }

    public String getPartyDetail() {
        return partyDetail;
    }

    public void setPartyDetail(String partyDetail) {
        this.partyDetail = partyDetail;
    }

    public String getFestEnd() {
        return festEnd;
    }

    public void setFestEnd(String festEnd) {
        this.festEnd = festEnd;
    }
}
